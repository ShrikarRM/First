
public class CheckVowels {

	public static void main(String[] args) {
		
		System.out.println("Start prog");
		char character = 'b';
		// A,E,I,O,U
		switch (character) {
		case 'A':
			System.out.println("The char is vowel");
			break;

		case 'E':
			System.out.println("The char is vowel");
			break;

		case 'I':
			System.out.println("The char is vowel");
			break;

		case 'o':
			System.out.println("The char is vowel");
			break;

		default:
			System.out.println("The char is not a vowel");
			break;

		}
		System.out.println("End prog");

	}

}
